class main { //クラス部分
  public static void main (String[] args) {
    System.out.println("hello Java");
    //数値はダブルクオーテーションは不要。計算も可能。" "で囲むとそのまま出力される。
    System.out.println(3);
    System.out.println(5+5);
    //文字列を足し算できる。連結できる。
    System.out.println("5"+"6");
    System.out.println("Let's"+"Party");
  }
}